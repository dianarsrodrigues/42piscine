#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;
	int	j;
	int a;
	
	i = 0;
	a = 'a';
	j = 1;
	if (argc == 2)
	{
		while (argv[1][i] != '\0')
		{
			j = (argv[1][i] - a); 
			write (1, &argv[1][i], 1);
			i++;
		}
	}
	return (0);
}
