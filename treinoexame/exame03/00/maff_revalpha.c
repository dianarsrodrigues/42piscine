#include <unistd.h>

int	main(void)
{
	int	i;
	int	j;
	
	i = 'z';
	j = 'Y';
	while (i >= 'b' && j >= 'A')
	{
		write (1, &i, 1);
		write (1, &j, 1);
		i -= 2;
		j -= 2;
	}
	return (0);
}
